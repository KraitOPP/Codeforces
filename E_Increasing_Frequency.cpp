signed main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, c;
    cin >> n >> c;
    vector<int> a(n);
    int maxv=0;
    for(int i=0; i<n; i++){
        cin>>a[i];
        maxv=max(maxv, a[i]);
    }

    vector<vector<int>> pos(maxv+1);
    for(int i=0; i<n; i++){
        pos[a[i]].push_back(i);
    }

    vector<int> pre(n);
    pre[0]=(a[0]==c);
    for(int i=1; i<n; i++)
        pre[i]=pre[i-1]+(a[i]==c);

    int freqc=pre[n-1];
    int res=freqc;

    for(int v=1; v<=maxv; v++){
        if(v==c || pos[v].empty()) continue;
        auto &arr=pos[v];
        int m=arr.size();

        int minB=-(arr[0]>0 ? pre[arr[0]-1] : 0);
        int best_sc=LLONG_MIN;

        for(int j=0; j<m; j++){
            int A=(j+1) - pre[arr[j]];
            best_sc=max(best_sc, A-minB);

            if(j+1 < m){
                int B = (j+1) - (arr[j+1] > 0 ? pre[arr[j+1]-1] : 0);
                minB = min(minB, B);
            }
        }

        if(best_sc>0)
            res=max(res, freqc + best_sc);
    }

    cout<<res<<endl;
    return 0;
}
