signed main() {
    fastio();
    int n;
    cin>>n;
    vi a(n);
    inp(a,n);
    vector<vector<int>> pre(n, vector<int>(32,0));
    for(int i=0;i<32;i++){
        if((a[0]&(1LL<<i))>0) pre[0][i]++;
    }
    for(int i=1;i<n;i++){
        pre[i]=pre[i-1];
        for(int j=0;j<32;j++){
            int num=1LL<<j;
            if((num&a[i])>0) pre[i][j]++;
            pre[i][j]%=2;
        }
    }
    int res=0;
    for(int i=0;i<32;i++){
        int sum0=0, sum1=0, curr=0;
        int cnt0=1,cnt1=0;
        for(int j=0;j<n;j++){
            if(pre[j][i]==0){
                curr+=cnt1*(j+1)-sum1;
                sum0+=j+1;
                cnt0++;
                curr%=MOD;
            }
            else{
                curr+=cnt0*(j+1)-sum0;
                sum1+=j+1;
                cnt1++;
                curr%=MOD;
            }
            // if(i<5) cout<<curr<<" ";
        }
        // cout<<endl;
        res+=(1LL<<i)*curr;
        res%=MOD;
    }

    cout<<res<<endl;

    return 0;
}