int solve(vector<int>& a, vi &nums) {
    int n=a.size();
    vector<vector<int>> dp(n+1, vector<int>(64, 0));
    dp[0][63] = 1;

    for(int i=0; i<n; i++) {
        for (int curr=0; curr<64; curr++){
            if (dp[i][curr]==0) continue; 
            dp[i+1][curr]=(dp[i+1][curr]+dp[i][curr])%MOD;
            dp[i+1][curr&a[i]]=(dp[i+1][curr&a[i]]+dp[i][curr])%MOD;
        }
    }
    
    int res=0;
    for(auto num: nums){
        if(num==63){
            res=(res+dp[n][num]-1)%MOD;
        }
        else res=(res+dp[n][num])%MOD;
    }

    return res;
}

 
