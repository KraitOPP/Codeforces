signed main() {
    fastio();

    int n, m;
    cin >> n >> m;

    vector<vector<pair<int, int>>> adj(n);
    for (int i = 0; i < m; i++) {
        int u, v, w;
        cin >> u >> v >> w;
        u--, v--; 
        if (u == v) continue;
        adj[u].push_back({v, w});
        adj[v].push_back({u, w});
    }

    vector<int> dist(n, INF), parent(n, -1);
    priority_queue<pair<int, int>, vector<pair<int, int>>, greater<pair<int, int>>> q;

    dist[0] = 0;
    q.push({0, 0});

    while (!q.empty()) {
        int u = q.top().second;
        int d = q.top().first;
        q.pop();

        if (d > dist[u]) continue;

        for (auto v : adj[u]) {
            if (dist[v.first] > d + v.second) {
                dist[v.first] = d + v.second;
                parent[v.first] = u;
                q.push({dist[v.first], v.first});
            }
        }
    }

    vector<int> res;
    int target = n - 1;
    if (dist[target] == INF) {
        cout << -1 << endl;
        return 0;
    }

    while (target != -1) {
        res.push_back(target);
        target = parent[target];
    }
    reverse(res.begin(), res.end());

    for (auto i : res) {
        cout << i + 1 << " "; 
    }
    cout << endl;

    return 0;
}
