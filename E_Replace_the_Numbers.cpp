#define int long long
#define MAXN 500001
int parent[MAXN];


signed main() {
    fastio();
    int q;
    cin >> q;
    vector<vector<int>> que;
    for(int i=0; i<q; i++){
        int type;
        cin>>type;
        if(type==1) {
            int x;
            cin>>x;
            que.push_back({type, x});
        } 
        else{
            int x, y;
            cin>>x>>y;
            que.push_back({type, x, y});
        }
    }

    vector<int> res;
    
    for(int i=0; i<MAXN; i++) parent[i]=i;
    for(int i=q-1; i>=0; i--){
        int type=que[i][0];
        if(type==1){
            int x=que[i][1];
            res.push_back(parent[x]);
        } 
        else{
            int x=que[i][1];
            int y=que[i][2];
            parent[x]=parent[y];
        }
    }

    reverse(res.begin(), res.end());

    for(int i=0; i<res.size();i++) {
        cout<<res[i]<<" ";
    }
    cout<<endl;

    return 0;
}
