signed main() {
    fastio();
    int n;
    cin>>n;
    vector<vector<int>> a(26);
    vector<string> strs;
    for(int i=0;i<n;i++){
        string s;
        cin>>s;
        strs.push_back(s);
        set<char> st;
        for(int j=0;j<s.length();j++){
            st.insert(s[j]);
        }
        for(auto c: st){
            a[c-'a'].pb(i);
        }
    }
    queue<int> q;
    vector<int> vis(n,0), visalp(26,0);
    vector<int> comp(n,-1);
    q.push(0);
    vis[0]=1;
    while(!q.empty()){
        int u = q.front();
        q.pop();
        for(int i=0;i<strs[u].size();i++){
            char v = strs[u][i];
            if(visalp[v-'a']) continue;
            visalp[v-'a']=1;
            for(auto neigh: a[v-'a']){
                if(vis[neigh]) continue;    
                comp[neigh]=u;
                vis[neigh]=1;
                q.push(neigh);
            }
        }
    }
    int res=0;
    for(int i=0;i<n;i++) res+=comp[i]==-1;
    cout<<res<<endl;
    return 0;
}