#define int long long

bool possible = true;
 
vector<int> dfs(int node, int parent, vector<vector<int>> &adj, vector<int> &c) {
    vector<int> order;
    for (int child : adj[node]) {
        if (child == parent) continue;
        vector<int> subOrder = dfs(child, node, adj, c);
        if (!possible) return order;
        for (int x : subOrder) {
            order.push_back(x);
        }
    }
    if (c[node] > order.size()) {
        possible = false;
        return order;
    }

    order.insert(order.begin() + c[node], node);
    return order;
}
 
signed main(){
    fastio();
    int n;
    cin>>n;
    vector<vector<int>> adj(n);
    vector<int> c(n);
    int root=-1;
    for (int i=0; i<n; i++){
        int par;
        cin>>par>>c[i];
        if(par==0){
            root=i;
        } 
        else{
            par--; 
            adj[par].push_back(i);
            adj[i].push_back(par);
        }
    }
 
    vector<int> order=dfs(root, -1, adj, c);
    if(!possible){
        cout<<"NO"<<endl;
        return 0;
    }

    vector<int> ans(n);
    for (int i=0; i<n; i++){
        ans[order[i]]=i+1;
    }
 
    cout<<"YES"<<endl;
    for(int i=0; i<n; i++){
        cout<<ans[i]<<" ";
    }
    cout<<endl;
 
    return 0;
}
