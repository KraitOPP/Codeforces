signed main() {
    fastio();
    int t;
    cin >> t;
    while (t--) {
        int n,x;
        cin>>n>>x;
        vector<bool> m(32, false);
        for(int i=0;i<31;i++){
            int num = 1LL<<i;
            if((num&x)>0){
                m[i]=true;
            }
        }
        // debug(m);
        vi res(n,0);
        vector<bool> mp(32,false);
        for(int i=0;i<n;i++){
            bool flg=true;
            for(int j=0;j<31;j++){
                int num = 1LL<<j;
                if(m[j]){
                    continue;
                }
                else if((i&num)>0){
                    flg=false;
                    break;
                }
            }
            if(flg){
                res[i]=i;
                for(int j=0;j<31;j++){
                    int num = 1LL<<j;
                    if((i&num)>0){
                        mp[j]=true;
                    }
                }
            }
            else break;
        }
        int num=0;
        for(int i=0;i<32;i++){
            if(m[i] && !mp[i]){
                num+=1LL<<i;
            }
        }
        res[n-1]|=num;
        for(int i=0;i<n;i++) cout<<res[i]<<" ";
        cout<<endl;
    }
    return 0;
}