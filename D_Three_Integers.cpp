const int MAXN = 2e4+1;
vector<vector<int>> divi(MAXN);


signed main() {
    fastio();
    for(int i=1;i<=2e4;i++){
        for(int j=i;j<=2e4;j+=i){
            divi[j].pb(i);
        }
    }

    int t;
    cin>>t;
    while(t--){
        int a,b,c;
        cin>>a>>b>>c;
        int res=INT_MAX, resa=-1, resb=-1, resc=-1;
        for(int i=1;i<=2e4;i++){
            int curra=0, currb=i, currc=0;
            int costa=INT_MAX, costb=abs(i-b), costc=INT_MAX;
            for(auto num: divi[i]){
                if(abs(num-a)<costa){
                    costa=abs(num-a);
                    curra=num;
                }
            }
            int forc = (c+i-1)/i;
            costc=abs(forc*i-c);
            currc = forc*i;
            if(forc!=1 && (abs((forc-1)*i-c)<costc)){
                costc=abs((forc-1)*i-c);
                currc=(forc-1)*i;
            }

            if(costa+costb+costc<res){
                res=costa+costb+costc;
                resa=curra;
                resb=currb;
                resc=currc;
            }
        }

        cout<<res<<endl;
        cout<<resa<<" "<<resb<<" "<<resc<<endl;
    }
    return 0;
}