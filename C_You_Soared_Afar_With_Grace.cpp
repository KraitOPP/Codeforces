signed main() {
    fastio();
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin>>n;
        vi a(n), b(n);
        inp(a,n); inp(b,n);
        map<int,int> mp;
        for(int i=0;i<n;i++) mp[b[i]]=i;
        int i=0, j=n-1;
        bool flg=true, f=false;
        for(int i=0;i<n;i++){
            if(a[i]!=b[i]) f=true;
        }
        if(!f){
            cout<<-1<<endl;
            continue;
        }
        vpii res;
        while(i<j){
            if(a[i]==b[i] && a[j]==b[j]){
                break;
            }
            if(a[i]==b[j]){
                i++;
                j--;
                continue;
            }
            if(mp[a[i]]<i){
                flg=false;
                break;
            }
            else{
                int tmp=mp[a[i]];
                mp[a[i]]=j;
                mp[b[j]]=tmp;
                swap(a[j], a[tmp]);
                swap(b[j], b[tmp]);
                res.pb({tmp+1, j+1});
                if(a[i]==b[j]){
                    i++;
                    j--;
                }
            }
        }
        for(int i=0;i<n;i++){
            if(a[i]!=b[n-1-i]) flg=false;
        }
        if(!flg){
            cout<<-1<<endl;
        }
        else{
            cout<<res.size()<<endl;
            for(auto itr: res) cout<<itr.ff<<" "<<itr.ss<<endl;
        }
    }
    return 0;
}