void dfs(vector<vector<int>> &adj, int node, int par, vector<int> &subtree) {
    subtree[node] = 1;
    for(auto neigh : adj[node]){
        if(neigh == par) continue;
        dfs(adj, neigh, node, subtree);
        subtree[node] += subtree[neigh];
    }
}


int modInverse(int A, int M) {
    int m0 = M, y = 0, x = 1;
    if (M == 1)
        return 0;
    while (A > 1) {
        int q = A / M;
        int t = M;
        M = A % M, A = t;
        t = y;
        y = x - q * y;
        x = t;
    }
    if (x < 0)
        x += m0;
    return x;
}

