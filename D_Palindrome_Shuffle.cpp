bool isPos(string &s, int n, int mid){
    int i=0, j=n-1;
    while(i<j && s[i]==s[j]) i++,j--;
    if(i>j) return true;
    vi used(n,0);
    // cout<<mid<<" "<<i<<" "<<j<<" - ";
    if(i+mid>j) return true;
    map<char, int> mp1,mp2;
    for(int k=i;k<mid+i;k++){
        used[k]=true;
        used[n-1-k]=true;
        mp1[s[k]]++;
        if(n-1-k>=(i+mid)){
            mp2[s[n-1-k]]++;
        }
    }
    for(auto ch: mp2){
        if(mp1[ch.ff]<mp2[ch.ff]) return false;
    }
    for(int i=0;i<n;i++){
        if(used[i]) continue;
        if(s[i]!=s[n-1-i]) return false;
    }

    return true;
}

