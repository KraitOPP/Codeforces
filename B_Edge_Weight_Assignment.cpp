bool odd=false;
int y=0;

int dfs(vector<vi> &adj, int node, int par, vector<bool> &p){
    int res=1;
    bool isOdd=false, isEven=false;
    bool isChild=false;
    for(auto neigh: adj[node]){
        if(neigh==par) continue;
        int x = dfs(adj, neigh, node, p);
        isOdd|=(x%2==1);
        isEven|=(x%2==0);
        if(x==1){
            p[neigh]=true;
            // cout<<neigh<<" ";
            isChild=true;
        }
        res=max(res, 1+x);
    }

    odd|=(isOdd&&isEven);
    if(isChild) y++;

    return res;
}



